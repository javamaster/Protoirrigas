    private void jbPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPesquisarActionPerformed
        
        String pesquisaText = tfPesquisar.getText().trim();
        ArrayList<Cultura> culturas = null;
        
        try {
            
        if(!pesquisaText.isEmpty()){
        culturas = FindCultureAtBase(pesquisaText);
        }
        else{        
            culturas = findAllCultures();
        }                    
            CulturaTableModel tm = new CulturaTableModel(culturas);            
            jtCultura.setModel(tm);        
        
        } catch (Exception e) {
            
            e.printStackTrace();
            
        }
    }//GEN-LAST:event_jbPesquisarActionPerformed

