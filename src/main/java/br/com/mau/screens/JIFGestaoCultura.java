    private void listenerTable() {
        getTable().addMouseListener(new MouseAdapter() {

            @Override
            public void mouseClicked(MouseEvent e) {
                if(e.getClickCount() == 2){                                          
                    Cultura c = getSelectedCultura();
                    if(c != null){
                        show(c);
                    }                    
                }
            }
            
        });
    }
    
    public Cultura getSelectedCultura(){
        int line = getTable().getSelectedRow();
        if(line != -1){
            return ((CulturaTableModel)getTable().getModel()).getCulturaAt(line);
        }
        return null;
    }
    
    public void show(Cultura c){
        JIFCultura frame = new JIFCultura();
        frame.setCultura(c);
        frame.setTitle("Editar Cultura");
        getParent().add(frame);
        frame.setVisible(true);
    }
