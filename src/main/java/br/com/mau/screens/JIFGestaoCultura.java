    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        
        GenericDAO dao = new GenericDAO(JPAUtil.createEntityManager(), Cultura.class);
        Cultura c = getSelectedCultura();
        if(c == null){
            JOptionPane.showMessageDialog(null, "Selecione uma linha!!");
        }
        else{
            int resp = JOptionPane.showConfirmDialog(null, "Deseja realmente Excluir?", "Exclusão",
                    JOptionPane.YES_NO_OPTION);

            if(resp == JOptionPane.YES_OPTION){
                dao.delete(c.getId());
            }
        }
    }//GEN-LAST:event_jbExcluirActionPerformed

    private void jbEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEditarActionPerformed
        
        Cultura c = getSelectedCultura();
        
        if(c == null){
            JOptionPane.showMessageDialog(null, "Selecione uma linha!!");
        }
        else{
            show(c);
        }
        
    }//GEN-LAST:event_jbEditarActionPerformed

    
    private ArrayList<Cultura> FindCultureAtBase(String nome){
        GenericDAO dao = new GenericDAO(JPAUtil.createEntityManager(), Cultura.class);
        return (ArrayList<Cultura>) dao.findByName(nome);
        
    }
    
    private ArrayList<Cultura> findAllCultures(){
        GenericDAO dao = new GenericDAO(JPAUtil.createEntityManager(), Cultura.class);
        return (ArrayList<Cultura>) dao.findAll();
    }
    
